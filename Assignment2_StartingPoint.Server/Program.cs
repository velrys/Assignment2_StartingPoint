using Assignment2_StartingPoint.Application.Handlers;
using Assignment2_StartingPoint.Domain;
using Assignment2_StartingPoint.Infrastructure.MySQL_DatabaseGateway;
using MediatR;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.AddMediatR(typeof(BaseHandler<,>));
builder.Services.AddScoped<IDatabaseGatewayFacade, DatabaseGatewayFacade>();
var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
